    public void updateLists() {
        try {
            
            preparedStatement = conn.prepareStatement("SELECT bill_id,chequenum FROM cheque_info WHERE status = '0'");
            rs = preparedStatement.executeQuery();
            System.out.println("rs " + rs);
            jlPending.setModel(DbUtils.resultSetToTableModel(rs));
            preparedStatement = conn.prepareStatement("SELECT bill_id,chequenum FROM cheque_info WHERE status = 1");
            rs = preparedStatement.executeQuery();
            jlCleared.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Error in update list : " + e);
        
        }
    }

