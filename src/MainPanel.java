    public void barChartDemo(){
        try {
            pnlBarChartDemo.removeAll();
            String sql = "SELECT productname,stock from product WHERE stock < 15 ";
            JDBCCategoryDataset dataset = new JDBCCategoryDataset(MySqlConnect.connectDB(), sql);

             JFreeChart barChart;

             barChart = ChartFactory.createBarChart("", "Product Name", "Stock", dataset, PlotOrientation.VERTICAL, false, true, true);
             ChartPanel cp = new ChartPanel(barChart);
             pnlBarChartDemo.add(cp);
             CategoryPlot p = (CategoryPlot) barChart.getPlot();
             BarRenderer renderer = (BarRenderer) p.getRenderer();
             renderer.setBarPainter(new StandardBarPainter());
             renderer.setSeriesPaint(0, new Color(12, 206, 107));
             renderer.setSeriesPaint(1, new Color(220, 237, 49));
             renderer.setSeriesPaint(2, new Color(239, 45, 86));

             p.getDomainAxis().setTickLabelPaint(Constants.LIGHT_BLUE);
             p.getDomainAxis().setAxisLinePaint(Constants.LIGHT_BLUE);
             p.getDomainAxis().setLabelPaint(Constants.LIGHT_BLUE);

             p.getRangeAxis().setTickLabelPaint(Constants.LIGHT_BLUE);
             p.getRangeAxis().setAxisLinePaint(Constants.LIGHT_BLUE);
             p.getRangeAxis().setLabelPaint(Constants.LIGHT_BLUE);
             Font f = new Font("Arial", 1, 18);
             p.getRangeAxis().setLabelFont(f);
             p.setRangeGridlinePaint(new Color(0, 246, 237));
             p.setBackgroundPaint(new Color(29, 35, 59));
             barChart.setBackgroundPaint(new Color(29, 35, 59));
             p.setOutlinePaint(new Color(29, 35, 59));
             barChart.removeLegend();
        } catch (Exception e) {
            System.out.println("Some error in bar chart : " + e);
        }

    }
