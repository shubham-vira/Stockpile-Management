    private void jtbDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbDataMouseClicked
        btnAdd.setEnabled(false);
        selectedRow = jtbData.getSelectedRow();
        selectedCustId = jtbData.getModel().getValueAt(selectedRow, 0).toString();
        try {
            sql = "SELECT * FROM customer WHERE customerid = " + selectedCustId;
            preparedStatement = conn.prepareStatement(sql);
            rs = preparedStatement.executeQuery();
            if(rs.next()){
                txtCustName.setText(rs.getString("customername"));
                txtCustContact.setText(rs.getString("contactno"));
                txtcustGST.setText(rs.getString("gst"));
                txtCustEmail.setText(rs.getString("email"));
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Some Error : " + e);
        }
    }//GEN-LAST:event_jtbDataMouseClicked

