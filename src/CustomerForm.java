     void updateTableData(String sql){
        try {
            preparedStatement = conn.prepareStatement(sql);
            rs = preparedStatement.executeQuery();
            jtbData.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Issue : " + e);
        }
        finally{
            try {
                rs.close();
                preparedStatement.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Problem in closing rs or prepared statement :  " + e);
            }
        }
    }
     
     private void checkEmail(){
        String emailPatternString = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-\\+]+)*@[_A-Za-z0-9-]+(\\.A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
	Pattern emailPattern = Pattern.compile(emailPatternString);
	Matcher emailMatcher = emailPattern.matcher(txtCustEmail.getText());
	if(emailMatcher.matches()){
            txtCustEmail.setForeground(Constants.LIGHT_BLUE);
            jspEmail.setBackground(Constants.LIGHT_BLUE);
            jspEmail.setForeground(Constants.LIGHT_BLUE);
        }
        else{
            txtCustEmail.setForeground(Constants.RED);
            jspEmail.setBackground(Constants.RED);
            jspEmail.setForeground(Constants.RED);
        }
    }
    
    
